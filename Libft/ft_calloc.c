#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;
	size_t	abc;

	abc = nmemb * size;
	ptr = malloc(abc);
	ft_bzero(ptr, abc);
	return (ptr);
}
